// Реализовать четыре основные арифметические операции в виде функций с двумя параметрами. Обязательно использовать оператор return.
function sum(a, d) {
    return (a + d);
}
function diff(a, d) {
    return (a - d);
}
function mult(a, d) {
    return (a * d);
}
function div(a, d) {
    return (a / d);
}
console.log(sum(1, 3))
console.log(diff(1, 3))
console.log(mult(1, 3))
console.log(div(1, 3))